package com.fh.shop.api.product.vo;

import java.io.Serializable;

public class ProductVo implements Serializable {

    private static final long serialVersionUID = 43206732710623885L;

    private Integer id;

    private String name;

    private Float price;

    private String photoName;

    public Integer getId() {
        return id;
    }

    public ProductVo setId(Integer id) {
        this.id = id;
        return this;
    }

    public String getName() {
        return name;
    }

    public ProductVo setName(String name) {
        this.name = name;
        return this;
    }

    public Float getPrice() {
        return price;
    }

    public ProductVo setPrice(Float price) {
        this.price = price;
        return this;
    }

    public String getPhotoName() {
        return photoName;
    }

    public ProductVo setPhotoName(String photoName) {
        this.photoName = photoName;
        return this;
    }
}
